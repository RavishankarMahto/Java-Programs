public class Constantt {
    public static void main(String[] args){
        int a=30;
        int b=40;
        int c=50;
        int sum = a+b+c;
        System.out.println(sum);

// use here final keyword when use final keyword then after no change this value
// final makes constant
        final float PI = 3.14F;
        // PI = 1.21F;
        System.out.println(PI);

    }
    
}
